/**
 * Rewrites each package's src/version.ts from its package.json.
 *
 * changesets bumps package.json only, so a hand-maintained version.ts drifts
 * silently — at 0.5.0 the SDK still reported 0.4.0 in X-Sonilo-Client-Version
 * on every request. Chained onto the root `version` script so the constant is
 * regenerated as part of the same bump that moves package.json.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

const TARGETS = [
  ["packages/sonilo", "The SDK's own version."],
  ["packages/cli", "The CLI's own version — not the SDK's, which it merely wraps."],
  ["packages/sonilo-video-kit", "The kit's own version."],
];

let changed = 0;
for (const [pkgDir, note] of TARGETS) {
  const pkg = JSON.parse(readFileSync(join(root, pkgDir, "package.json"), "utf8"));
  const file = join(root, pkgDir, "src/version.ts");
  const next = `/** ${note} Generated by scripts/sync-versions.mjs — do not edit. */\nexport const VERSION = ${JSON.stringify(pkg.version)};\n`;
  const prev = readFileSync(file, "utf8");
  if (prev !== next) {
    writeFileSync(file, next);
    console.log(`synced ${pkgDir}/src/version.ts -> ${pkg.version}`);
    changed++;
  }
}
console.log(changed ? `${changed} file(s) synced` : "all versions already in sync");
